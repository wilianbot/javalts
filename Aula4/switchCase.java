package Aula4;
public class switchCase {
    public static void main(String[] args) {
        int n = 2;
        switch (n) {
            case 1:
                System.out.println("A Variavel Vale 1");
                break;
            case 2:
                System.out.println("A Variavel Vale 2");
                break;
            case 3:
                System.out.println("A Variavel Vale 3");
                break;
        }
    }
}
