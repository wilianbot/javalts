package Aula3;
public class operadores {
    public static void main(String[] args) {
        int x;
        // Operador binario onde x é o operando
        x = 1;
        // Operador unário onde x é o operando
        x++;
        System.out.println(x);
        // Operador ternario onde x, "Maior" e "Menor" são operandos
        System.out.println(x > 2 ? "Maior" : "Menor");
        char c = 'A';
        System.out.println(c);
    }
}
