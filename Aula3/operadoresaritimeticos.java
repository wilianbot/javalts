package Aula3;


public class operadoresaritimeticos {
    public static void main(String[] args) {
        // Variáveis numéricas
        int i = 37;
        int j = 42;
        double x = 27.475;
        double y= 7.22;
        System.out.println("Valores das variavéis...");
        System.out.println("  i = " +i);
        System.out.println("  j = " +j);
        System.out.println("  x = " +i);
        System.out.println("  y = " +i);

        // Somando os numeros
        System.out.println("Somando...");
        System.out.println("  i+j = " + (i+j));
        System.out.println("  x+y = " + (x+j));

        // Subtraindo os numeros
        System.out.println("Subtraindo...");
        System.out.println(" i-j =" + (i-j));
        System.out.println(" x-y =" + (x-y));

        // Multiplicando numeros
        System.out.println("Multiplicando...");
        System.out.println(" i*j =" + (i*j));
        System.out.println(" x*y =" + (x*y));

        // Dividindo os numeros
        System.out.println("Dividindo...");
        System.out.println(" i/j =" + (i/j));
        System.out.println(" x/y =" + (x/y));

        // Calculando o módulo
        System.out.println("Calculando Modulo...");
        System.out.println(" i%j =" + (i%j));
        System.out.println(" x%y =" + (x%y));

        // Misturando tipos
        System.out.println("Misturando tipos...");
        System.out.println(" i+y =" + (i+y));
        System.out.println(" x*x =" + (i*x));

        
    }
}
